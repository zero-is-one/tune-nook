#!/usr/bin/env node

import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const DEFAULT_SOURCE_PATH = path.resolve(
  "scripts",
  "thesession-top5000-youtube-ids.json",
);
const DEFAULT_LIBRARY_PATH = path.resolve("src", "assets", "library.json");
const DEFAULT_AUTHOR_PREFIX = "The Session";

function parseArgs(argv) {
  const options = {
    sourcePath: DEFAULT_SOURCE_PATH,
    libraryPath: DEFAULT_LIBRARY_PATH,
    authorPrefix: DEFAULT_AUTHOR_PREFIX,
    dryRun: false,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];

    if (arg === "--source" && argv[i + 1]) {
      options.sourcePath = path.resolve(argv[i + 1]);
      i += 1;
      continue;
    }

    if (arg === "--library" && argv[i + 1]) {
      options.libraryPath = path.resolve(argv[i + 1]);
      i += 1;
      continue;
    }

    if (arg === "--author-prefix" && argv[i + 1]) {
      options.authorPrefix = argv[i + 1];
      i += 1;
      continue;
    }

    if (arg === "--dry-run") {
      options.dryRun = true;
      continue;
    }

    if (arg === "--help" || arg === "-h") {
      printHelp();
      process.exit(0);
    }
  }

  return options;
}

function printHelp() {
  process.stdout.write(
    [
      "Usage:",
      "  node scripts/append-session-videos-to-library.mjs [options]",
      "",
      "Options:",
      "  --source <path>   Input JSON generated by fetch-session-youtube-ids.mjs",
      "  --library <path>  Library JSON array to append to",
      "  --author-prefix <name>  Author prefix for appended records (default: The Session)",
      "  --dry-run         Compute and report changes without writing files",
      "  --help, -h        Show this message",
      "",
      "Appended record format:",
      "  title: {Tunename}",
      "  author: The Session - {video id} ({type})",
    ].join("\n") + "\n",
  );
}

async function readJson(filePath) {
  try {
    const raw = await readFile(filePath, "utf8");
    return JSON.parse(raw);
  } catch (error) {
    const reason = error?.message || String(error);
    throw new Error(`Failed to read JSON file: ${filePath}. Reason: ${reason}`);
  }
}

function extractYoutubeIdFromUrl(url) {
  if (typeof url !== "string" || url.trim() === "") {
    return null;
  }

  try {
    const parsed = new URL(url);

    if (parsed.hostname.includes("youtube.com")) {
      const id = parsed.searchParams.get("v");
      return id && id.trim() ? id.trim() : null;
    }

    if (parsed.hostname === "youtu.be") {
      const id = parsed.pathname.replace(/^\//, "").trim();
      return id || null;
    }
  } catch {
    return null;
  }

  return null;
}

function asSafeText(value, fallback) {
  if (typeof value !== "string") {
    return fallback;
  }

  const trimmed = value.trim();
  return trimmed || fallback;
}

function formatAuthor(authorPrefix, youtubeId, tuneType) {
  return `${authorPrefix} - ${youtubeId} (${tuneType})`;
}

function collectExistingYoutubeIds(libraryEntries) {
  const ids = new Set();

  for (const entry of libraryEntries) {
    const id = extractYoutubeIdFromUrl(entry?.url);
    if (id) {
      ids.add(id);
    }
  }

  return ids;
}

function buildEntriesToAppend(sourceTunes, existingIds, authorPrefix) {
  const entries = [];
  const plannedIds = new Set();

  const stats = {
    inspectedTunes: 0,
    inspectedVideos: 0,
    skippedExisting: 0,
    skippedDuplicateInSource: 0,
    skippedInvalid: 0,
    appended: 0,
  };

  for (const tune of sourceTunes) {
    stats.inspectedTunes += 1;

    const tuneName = asSafeText(tune?.name, "Unknown Tune");
    const tuneType = asSafeText(tune?.type, "unknown");
    const youtubeIds = Array.isArray(tune?.youtubeIds) ? tune.youtubeIds : [];

    for (const rawId of youtubeIds) {
      stats.inspectedVideos += 1;

      const youtubeId = asSafeText(rawId, "");
      if (!youtubeId) {
        stats.skippedInvalid += 1;
        continue;
      }

      if (existingIds.has(youtubeId)) {
        stats.skippedExisting += 1;
        continue;
      }

      if (plannedIds.has(youtubeId)) {
        stats.skippedDuplicateInSource += 1;
        continue;
      }

      plannedIds.add(youtubeId);
      entries.push({
        title: tuneName,
        author: formatAuthor(authorPrefix, youtubeId, tuneType),
        url: `https://www.youtube.com/watch?v=${youtubeId}`,
      });
      stats.appended += 1;
    }
  }

  return { entries, stats };
}

async function main() {
  const options = parseArgs(process.argv.slice(2));

  process.stdout.write(`Reading source: ${options.sourcePath}\n`);
  process.stdout.write(`Reading library: ${options.libraryPath}\n`);

  const source = await readJson(options.sourcePath);
  const library = await readJson(options.libraryPath);

  if (!Array.isArray(library)) {
    throw new Error(`Library file is not a JSON array: ${options.libraryPath}`);
  }

  if (!source || !Array.isArray(source.tunes)) {
    throw new Error(
      `Source file is missing a tunes array: ${options.sourcePath}`,
    );
  }

  const existingIds = collectExistingYoutubeIds(library);
  process.stdout.write(
    `Detected ${existingIds.size} existing YouTube IDs in library.\n`,
  );

  const { entries, stats } = buildEntriesToAppend(
    source.tunes,
    existingIds,
    options.authorPrefix,
  );

  process.stdout.write(`Scanned tunes: ${stats.inspectedTunes}\n`);
  process.stdout.write(`Scanned source videos: ${stats.inspectedVideos}\n`);
  process.stdout.write(
    `Skipped existing in library: ${stats.skippedExisting}\n`,
  );
  process.stdout.write(
    `Skipped duplicates inside source file: ${stats.skippedDuplicateInSource}\n`,
  );
  process.stdout.write(`Skipped invalid IDs: ${stats.skippedInvalid}\n`);
  process.stdout.write(`New videos to append: ${stats.appended}\n`);

  if (entries.length === 0) {
    process.stdout.write("No new videos to append. Library is unchanged.\n");
    return;
  }

  if (options.dryRun) {
    process.stdout.write(
      "Dry run enabled. No files were changed. Remove --dry-run to write updates.\n",
    );
    return;
  }

  const updatedLibrary = [...library, ...entries];
  await writeFile(
    options.libraryPath,
    `${JSON.stringify(updatedLibrary, null, 2)}\n`,
    "utf8",
  );

  process.stdout.write(
    `Appended ${entries.length} videos to ${options.libraryPath}\n`,
  );
}

main().catch((error) => {
  process.stderr.write("Failed to append session videos to library.\n");
  process.stderr.write(
    `${error?.name || "Error"}: ${error?.message || String(error)}\n`,
  );
  if (error?.stack) {
    process.stderr.write(`${error.stack}\n`);
  }
  process.exitCode = 1;
});
